x1 = 2  # binary 010
y1 = 5 # binary 101
z1 = x1 | y1 # bitwise OR operation. Result in binary: 111; in decimal: 7
print(f"z1 = {z1}") # z1 = 7
print(f"z1 = {z1:0b}")  # z1 = 111


x2 = 4  # binary 100
y2 = 5 # binary 101
z2 = x2 | y2 # bitwise OR operation. Result in binary: 101; in decimal: 5
print(f"z2 = {z2}") # z2 = 5
print(f"z2 = {z2:0b}")  # z2 = 101